function openmenu(){
document.getElementById('open').style.display='none';
document.getElementById('section-menu').style.cssFloat="none";
document.getElementById('close').style.display='inline';
document.getElementById('menutable').style.display='inline';
}
function closemenu(){
document.getElementById('open').style.display='inline';
document.getElementById('section-menu').style.cssFloat="left";
document.getElementById('close').style.display='none';
document.getElementById('menutable').style.display='none';
}